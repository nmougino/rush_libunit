#include "libft.h"

size_t	ft_nbrlen(int nb)
{
	size_t	i;

	i = 1;
	if (nb < 0)
	{
		nb *= -1;
		++i;
	}
	while (nb > 9)
	{
		++i;
		nb /= 10;
	}
	return (i);
}
