#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t				i;
	const unsigned char	*p;

	i = 0;
	p = s;
	while (i < n)
	{
		if (p[i] == (const unsigned char)c)
			return ((void*)((unsigned long)p + (unsigned long)i));
		++i;
	}
	return (NULL);
}
