#include "libft.h"

int		ft_atoi(const char *str)
{
	int i;
	int ans;
	int neg;

	ans = 0;
	neg = 1;
	i = 0;
	while ((str[i] == '\n') || (str[i] == '\t') || (str[i] == '\v') ||
			(str[i] == ' ') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		neg = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] && ft_isdigit(str[i]))
		ans = (ans * 10) + str[i++] - '0';
	return (ans * neg);
}
